from fractions import Fraction


frac1 = "1/2"
frac2 = "1/3"


resultSum = Fraction(frac1) + Fraction(frac2)
resultMult = Fraction(frac1) * Fraction(frac2)
print(f"Сумма дробей: {resultSum}")
print(f"Произведение дробей: {resultMult}")

